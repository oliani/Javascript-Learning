var i = 0
/* While 
   Verifica e depois executa

*/
while(i < 6){
    console.log(i)
    i++;
}

i = 0

console.log('Segunto teste')

/* Do While
   Executa o bloco e depois verifica se deve repetir ou não
*/

do{
    console.log(i)
    i++
} while (i < 6)

/* Principal diferença
   O Do While garante que o bloco seja executado pelo menos uma vez, mesmo que a verificação fallhe depois
*/